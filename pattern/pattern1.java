import java.util.*;
public class pattern1 
{
	public static void main(String args[])
	{
	 Scanner sc=new Scanner(System.in);  

int val=sc.nextInt();
for(int i=0;i<val;i++)
{
	for(int j=0;j<val;j++)
	{
		System.out.print("*");
	}
	System.out.println();
}
	 
	}
}